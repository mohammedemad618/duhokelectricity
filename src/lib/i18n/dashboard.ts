const dictionaries: Record<Locale, DashboardDictionary> = {
  ar: arDashboard,
  en: enDashboard,
  ku: kuDashboard,
};

export async function getDashboardDictionary() {
  const locale = await getLocale();
  return {
    locale,
    dir: localeConfig[locale].dir,
    t: dictionaries[locale],
  };
}

export function dashboardDictionaryFor(locale: Locale): DashboardDictionary {
  return dictionaries[locale];
}
